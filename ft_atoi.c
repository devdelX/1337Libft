#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	int		res;
	int		sign;
	char	*x;

	i = 0;
	res = 0;
	sign = 1;
	x = (char *)str;
	while ((x[i] >= 9 && 13 >= x[i]) || x[i] == 32)
		i++;
	if (x[i] == '-' || x[i] == '+')
	{
		if (x[i] == '-')
			sign = -1;
		i++;
	}
	while (x[i] >= '0' && x[i] <= '9')
	{
		res = (res * 10) + x[i] - 48;
		i++;
	}
	return (res * sign);
}
